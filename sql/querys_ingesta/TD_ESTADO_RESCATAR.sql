SELECT
'RESCATAR' AS ESTADO
,STATUS
,STATIC
,PK
,FECHAMOVIMIENTOVALUE
,FECHAMOVIMIENTOINPUT
,CANCELREASONBANVALUE
,CANCELREASONBANINPUT
FROM INTERSEGURO.RESCATAR
UNION ALL
SELECT 
'RESCATARSURA' AS ESTADO
,STATUS
,STATIC
,PK
,FECHAMOVIMIENTOVALUE
,FECHAMOVIMIENTOINPUT
,NULL AS CANCELREASONBANVALUE
,NULL AS CANCELREASONBANINPUT
FROM INTERSEGURO.RESCATARSURA
)
