SELECT 
PROD.NUMERO_POLIZA
,PROD.ID_PRODUCTO
,PROD.ESTADO_CAUSAL
,UAT.ESTADO_CAUSAL
FROM POLIZA_PROD PROD
LEFT JOIN POLIZA_UAT UAT
ON PROD.NUMERO_POLIZA= UAT.NUMERO_POLIZA
AND PROD.ID_PRODUCTO= UAT.ID_PRODUCTO
WHERE (CASE WHEN TRIM(UPPER(PROD.ESTADO_CAUSAL))!=TRIM(UPPER(UAT.ESTADO_CAUSAL)) THEN 1 ELSE 0 END )=1
