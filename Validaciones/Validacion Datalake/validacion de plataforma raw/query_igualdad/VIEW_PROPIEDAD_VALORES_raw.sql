SELECT SUM(CASE WHEN PROD.PRID!=UAT.PRID THEN 1 ELSE 0 END) AS DIFF_PRID
,SUM(CASE WHEN TRIM(UPPER(PROD.SIMBOLO))!=TRIM(UPPER(UAT.SIMBOLO)) THEN 1 ELSE 0 END) AS DIFF_SIMBOLO
,SUM(CASE WHEN TRIM(UPPER(PROD.TIPO))!=TRIM(UPPER(UAT.TIPO)) THEN 1 ELSE 0 END) AS DIFF_TIPO
,SUM(CASE WHEN TRIM(UPPER(PROD.PTR_DISPLAY))!=TRIM(UPPER(UAT.PTR_DISPLAY)) THEN 1 ELSE 0 END) AS DIFF_PTR_DISPLAY
,SUM(CASE WHEN TRIM(UPPER(PROD.PTR_DISPLAY_OPCIONAL))!=TRIM(UPPER(UAT.PTR_DISPLAY_OPCIONAL)) THEN 1 ELSE 0 END) AS DIFF_PTR_DISPLAY_OPCIONAL
,SUM(CASE WHEN PROD.PTR_OPCION!=UAT.PTR_OPCION THEN 1 ELSE 0 END) AS DIFF_PTR_OPCION
,SUM(CASE WHEN PROD.TRID!=UAT.TRID THEN 1 ELSE 0 END) AS DIFF_TRID
,SUM(CASE WHEN TRIM(UPPER(PROD.TRDESC))!=TRIM(UPPER(UAT.TRDESC)) THEN 1 ELSE 0 END) AS DIFF_TRDESC
,SUM(CASE WHEN PROD.TRVAL!=UAT.TRVAL THEN 1 ELSE 0 END) AS DIFF_TRVAL
,SUM(CASE WHEN TRIM(UPPER(PROD.TR_DISPLAY))!=TRIM(UPPER(UAT.TR_DISPLAY)) THEN 1 ELSE 0 END) AS DIFF_TR_DISPLAY
,SUM(CASE WHEN TRIM(UPPER(PROD.TR_DISPLAY_OPCIONAL))!=TRIM(UPPER(UAT.TR_DISPLAY_OPCIONAL)) THEN 1 ELSE 0 END) AS DIFF_TR_DISPLAY_OPCIONAL
,SUM(CASE WHEN PROD.TR_OPCION!=UAT.TR_OPCION THEN 1 ELSE 0 END) AS DIFF_TR_OPCION
,SUM(CASE WHEN TRIM(UPPER(PROD.TIPOPROP))!=TRIM(UPPER(UAT.TIPOPROP)) THEN 1 ELSE 0 END) AS DIFF_TIPOPROP
,SUM(CASE WHEN PROD.FECHA_PROCESO!=UAT.FECHA_PROCESO THEN 1 ELSE 0 END) AS DIFF_FECHA_PROCESO